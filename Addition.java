class Addition
{
	public static void main(String[] args)
	{
	addition();
	addition1();
	addition2();
	addition3();
	addition4();
	addition5();
	}
public static void addition()
{
	int a=10;
	int b=20;
	int c=a+b;
	System.out.println(c);
}
public static void addition1()
{
	float d=12.4f;
	float e=13.5f;
	float f=d+e;
	System.out.println(f);
	
}
public static void addition2()
{
	double g=1.666;
	double h=1.888;
	double i=g+h;
	System.out.println(i);
}
public static void addition3()
{
	long j=1234;
	long k=2333;
	long l=j+k;
	System.out.println(l);
}
public static void addition4()
{	
	short m=333;
	short n=222;
	short o=(short)(m+n);
	System.out.println(o);
}
public static void addition5()
{
	byte p=120;
	byte q=90;
	byte r=(byte)(p+q);
	System.out.println(r);
}
}
	
	