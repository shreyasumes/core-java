class App {
	String name;
	String developer;

	public App(String name, String developer) {
		this.name = name;
		this.developer = developer;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setDeveloper(String developer) {
		this.developer = developer;
	}

	public String getDeveloper() {
		return developer;
	}
}


