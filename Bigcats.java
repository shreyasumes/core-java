class Bigcats {
	private String name;
	private String colour;

	// Setter for name
	public void setName(String name) {
		this.name = name;
	}

	// Getter for name
	public String getName() {
		return name;
	}

	// Setter for colour
	public void setColour(String colour) {
		this.colour = colour;
	}

	// Getter for colour
	public String getColour() {
		return colour;
	}
}
