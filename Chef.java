class Chef {
	String name;
	String speciality;

	public Chef(String name, String speciality) {
		this.name = name;
		this.speciality = speciality;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}

	public String getSpeciality() {
		return speciality;
	}
}

