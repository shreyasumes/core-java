class Method5
{
	public static void main(String[] args)
	{
		addition();
		subtraction();
		multiplication();
		division();
	}
public static void addition()
{
	int a=10;
	int b=20;
	int c = a+b;
	System.out.println(c);
}
public static void subtraction()
{
	int a=10;
	int b=30;
	int c=a-b;
	System.out.println(c);
}
public static void multiplication()
{
	int a=30;
	int b=40;
	int c=a*b;
	System.out.println(c);
}
public static void division()
{
	int a=10;
	int b=2;
	int c=a/b;
	System.out.println(c);
}
}