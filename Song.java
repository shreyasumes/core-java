class Song {
	String title;
	String singer;

	public Song(String title, String singer) {
		this.title = title;
		this.singer = singer;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getTitle() {
		return title;
	}

	public void setSinger(String singer) {
		this.singer = singer;
	}

	public String getSinger() {
		return singer;
	}
}


