class Tables{
	public static void main(String[] args){
		int n = 3;
		int m = 6;
		int o = 9;
		
		for(int i=1;i<=10;i++){
			System.out.println(n+ "*" +i +"=" +n*i);
		}
		System.out.println(" ");
		for(int i=1;i<=10;i++){
			System.out.println(m+ "*" +i +"=" +m*i);
		}
		System.out.println(" ");
		for(int i=1;i<=10;i++){
			System.out.println(o+ "*" +i +"=" +o*i);
		}	
	}
}